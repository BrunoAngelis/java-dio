package fundamentos;

public class SwitchCase {
    public static void main(String[] args) {
        char plano= 'B'; //M ; 'B';

        switch (plano){
            case 'T':{
                System.out.println("5G YOUTUBE");
            }
            case 'M':{
                System.out.printf("Whats e Instagram grátis");
            }
            case 'B':{
                System.out.println("100 minutos de ligação");
            }

        }
    }
}
